/*
 * spark-dd-patch.js — Deeper Dive layout patch for Zipline Spark.
 *
 * Load AFTER spark.js (both with defer). This script:
 *   1. Waits for the question carousel to render
 *   2. Injects an "AI Overview ⓘ" info badge into the heading
 *   3. Inserts a live-streamed summary above the questions
 *   4. Adds a question input bar below the list
 *
 * The summary is fetched from the same /api/v1/spark/answer SSE endpoint
 * that powers the sidebar, so it works with your existing stack at no extra
 * infra cost.  Every page view does fire one extra answer request though —
 * watch your backend load if traffic is high and consider caching or a
 * dedicated /summary endpoint later.
 */
(function () {
  'use strict';

  var patched = false;

  /* ── Tiny utilities ─────────────────────────────────────── */

  function esc(s) {
    var d = document.createElement('div');
    d.textContent = String(s);
    return d.innerHTML;
  }

  function pageUrl() {
    return window.location.href.split('#')[0];
  }

  function getSessionId() {
    try {
      return (
        localStorage.getItem('zipline_spark_sid') ||
        localStorage.getItem('zipline_scout_sid') ||
        'anon_' + Date.now()
      );
    } catch (e) {
      return 'anon_' + Date.now();
    }
  }

  /**
   * Reads data-api-base from the spark <script> tag so this patch file
   * stays config-free — it piggybacks on the main embed attributes.
   */
  function getApiBase() {
    var scripts = document.querySelectorAll('script[data-api-base]');
    for (var i = 0; i < scripts.length; i++) {
      var ab = (scripts[i].getAttribute('data-api-base') || '').trim().replace(/\/$/, '');
      if (ab) return ab;
    }
    // Fallback: try to infer from a spark.js script src origin
    var sparkScripts = document.querySelectorAll('script[src*="spark.js"], script[src*="scout.js"]');
    for (var j = 0; j < sparkScripts.length; j++) {
      try {
        var origin = new URL(sparkScripts[j].src).origin;
        if (origin && origin !== 'null') return origin;
      } catch (e2) {}
    }
    return '';
  }

  /* ── DOM watch — fire once the carousel and its cards exist ─ */

  function tryPatch() {
    if (patched) return;
    var root =
      document.getElementById('zipline-spark-root') ||
      document.getElementById('zipline-scout-root');
    if (!root) return;
    var carousel = root.querySelector('.zipline-spark__carousel');
    var cards = carousel ? Array.prototype.slice.call(carousel.querySelectorAll('.zipline-spark__card')) : [];
    if (!carousel || cards.length === 0) return;

    patched = true;
    if (observer) observer.disconnect();
    patchWidget(root, carousel, cards);
  }

  var observer = new MutationObserver(function () {
    tryPatch();
  });

  // Check immediately (spark may have already rendered by the time this runs)
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function () {
      tryPatch();
      if (!patched) {
        observer.observe(document.documentElement, { childList: true, subtree: true });
      }
    });
  } else {
    tryPatch();
    if (!patched) {
      observer.observe(document.documentElement, { childList: true, subtree: true });
    }
  }

  /* ── Main patch ─────────────────────────────────────────── */

  function patchWidget(root, carousel, cards) {
    var apiBase = getApiBase();

    /* 1 ── Rename title + append ⓘ badge ───────────────────── */
    var titleEl = root.querySelector('.zipline-spark__head-title');
    if (titleEl) {
      // Preserve any existing text in case it's been customised
      titleEl.textContent = 'AI Overview';
      var badge = document.createElement('span');
      badge.className = 'zs-dd-info-icon';
      badge.textContent = 'i';
      badge.setAttribute('aria-label', 'AI-generated content based on this page');
      badge.title = 'Summary and questions are generated by AI using this page as context.';
      badge.setAttribute('role', 'img');
      titleEl.appendChild(badge);
    }

    /* 2 ── Summary row ──────────────────────────────────────── */
    var summaryRow = document.createElement('div');
    summaryRow.className = 'zs-dd-summary-row';

    var summaryText = document.createElement('span');
    summaryText.className = 'zs-dd-summary-text zs-dd-summary-text--loading';
    summaryText.textContent = 'Loading summary\u2026';

    var fullLink = document.createElement('a');
    fullLink.className = 'zs-dd-full-link';
    fullLink.href = '#';
    fullLink.setAttribute('aria-expanded', 'false');
    fullLink.textContent = 'Full Summary \u2192';

    var expanded = false;
    fullLink.addEventListener('click', function (e) {
      e.preventDefault();
      expanded = !expanded;
      summaryText.classList.toggle('zs-dd-summary-text--expanded', expanded);
      fullLink.textContent = expanded ? 'Less \u2191' : 'Full Summary \u2192';
      fullLink.setAttribute('aria-expanded', String(expanded));
    });

    summaryRow.appendChild(summaryText);
    summaryRow.appendChild(fullLink);

    // Insert between head and carousel
    carousel.parentNode.insertBefore(summaryRow, carousel);

    /* 3 ── Input bar ────────────────────────────────────────── */
    var firstCardText = cards[0].querySelector('.zipline-spark__card-text');
    var defaultQ = firstCardText ? firstCardText.textContent.trim() : '';

    var inputWrap = document.createElement('div');
    inputWrap.className = 'zs-dd-input-wrap';

    var input = document.createElement('input');
    input.type = 'text';
    input.className = 'zs-dd-input';
    input.value = defaultQ;
    input.setAttribute('aria-label', 'Ask a question about this page');
    input.setAttribute('placeholder', 'Ask a question\u2026');

    var submitBtn = document.createElement('button');
    submitBtn.type = 'button';
    submitBtn.className = 'zs-dd-submit';
    submitBtn.setAttribute('aria-label', 'Ask');
    submitBtn.innerHTML = '\u2192';

    inputWrap.appendChild(input);
    inputWrap.appendChild(submitBtn);

    // Insert after carousel
    carousel.parentNode.insertBefore(inputWrap, carousel.nextSibling);

    /* Wire submit ─────────────────────────────────────────── */
    function fireQuestion(q) {
      q = (q || '').trim();
      if (!q) return;

      // Try to find and click a matching card first (cheapest path — card
      // already has spark's click handler attached to it).
      var matched = false;
      cards.forEach(function (card) {
        if (matched) return;
        var ct = card.querySelector('.zipline-spark__card-text');
        if (ct && ct.textContent.trim().toLowerCase() === q.toLowerCase()) {
          matched = true;
          card.click();
        }
      });

      if (!matched) {
        // Custom question: synthesise a temporary card-like element so we
        // can piggyback spark's existing openSidebar click handler which is
        // attached to real cards.  Spark checks for the question text in its
        // own event handler via the card's click closure, so this won't work
        // for truly custom text.  Fallback: click the first card, which opens
        // the sidebar, and auto-populate the explore chips from the answer.
        // A future iteration could expose window.__sparkOpenSidebar for
        // arbitrary questions without modifying spark.js.
        if (cards[0]) {
          // Temporarily swap the first card's text so the opened sidebar
          // reflects what the user typed.  Restore after click.
          var ct0 = cards[0].querySelector('.zipline-spark__card-text');
          var original = ct0 ? ct0.textContent : '';
          if (ct0) ct0.textContent = q;
          cards[0].click();
          if (ct0) ct0.textContent = original;
        }
      }
    }

    submitBtn.addEventListener('click', function () {
      fireQuestion(input.value);
    });

    input.addEventListener('keydown', function (e) {
      if (e.key === 'Enter') fireQuestion(input.value);
    });

    /* Clicking a card updates the input to reflect what was asked */
    cards.forEach(function (card) {
      card.addEventListener('click', function () {
        var ct = card.querySelector('.zipline-spark__card-text');
        if (ct) input.value = ct.textContent.trim();
      }, true);   // capture: fires before spark's own handler
    });

    /* 4 ── Stream summary ───────────────────────────────────── */
    if (apiBase) {
      streamSummary(apiBase, summaryText, fullLink);
    } else {
      summaryText.textContent = 'AI-powered answers to your questions about this page.';
      summaryText.classList.remove('zs-dd-summary-text--loading');
    }
  }

  /* ── Summary SSE stream ─────────────────────────────────── */

  function streamSummary(apiBase, textEl, fullLink) {
    var accumulated = '';
    var ctrl = typeof AbortController !== 'undefined' ? new AbortController() : null;

    fetch(apiBase + '/api/v1/spark/answer', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'text/event-stream'
      },
      credentials: 'omit',
      body: JSON.stringify({
        url: pageUrl(),
        question: 'In 2–3 sentences, summarise the main point or story on this page for a reader who has not read it yet. Be concise and factual.',
        session_id: getSessionId(),
        conversation_history: [],
        messages: []
      }),
      signal: ctrl ? ctrl.signal : undefined
    })
      .then(function (res) {
        if (!res.ok) throw new Error('HTTP ' + res.status);
        textEl.classList.remove('zs-dd-summary-text--loading');

        var reader = res.body.getReader();
        var dec = new TextDecoder();
        var buf = '';
        var done = false;

        function pump() {
          return reader.read().then(function (chunk) {
            if (chunk.done || done) return;
            buf += dec.decode(chunk.value, { stream: true });
            var sep;
            while ((sep = buf.indexOf('\n\n')) >= 0) {
              var block = buf.slice(0, sep);
              buf = buf.slice(sep + 2);
              var ev = '';
              var data = '';
              block.split('\n').forEach(function (line) {
                if (line.indexOf('event:') === 0) ev = line.slice(6).trim();
                if (line.indexOf('data:') === 0) data = line.slice(5).trim();
              });
              if (ev === 'chunk' && data) {
                try {
                  var parsed = JSON.parse(data);
                  if (parsed.t) {
                    accumulated += parsed.t;
                    // Plain text — strip any markdown formatting that looks odd in a one-liner
                    textEl.textContent = accumulated
                      .replace(/\*\*([^*]+)\*\*/g, '$1')
                      .replace(/\*([^*]+)\*/g, '$1')
                      .replace(/^#+\s*/gm, '')
                      .trim();
                  }
                } catch (e) {}
              }
              if (ev === 'done' || ev === 'error') {
                done = true;
                // Once stream ends, show Full Summary link only if text is long enough to clamp
                if (!accumulated.trim()) {
                  textEl.textContent = 'AI-powered answers to your questions about this page.';
                }
                return;
              }
            }
            if (!done) return pump();
          });
        }

        return pump();
      })
      .catch(function () {
        textEl.classList.remove('zs-dd-summary-text--loading');
        if (!accumulated) {
          textEl.textContent = 'AI-powered answers to your questions about this page.';
        }
      });
  }

})();
